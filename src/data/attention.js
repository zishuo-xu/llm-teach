/** 行归一化，并补齐上三角为 null（因果遮罩，看不到未来） */
function normalize(matrix) {
  const n = matrix.length
  return matrix.map((row, i) => {
    const sum = row.reduce((a, b) => a + b, 0)
    const full = row.map((w) => w / sum)
    while (full.length < n) full.push(null)
    return full
  })
}

